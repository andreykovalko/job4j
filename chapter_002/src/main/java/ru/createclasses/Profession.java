public class Profession {
    private String name;
    private String profession;

    /**
     * Method getName.
     * Returns name of patient.
     */
    public String getName() {
        return this.name;
    }
    /**
     * Method getProfession.
     * Returns profession.
     */
    public String getProfession() {
        return this.profession;
    }
}