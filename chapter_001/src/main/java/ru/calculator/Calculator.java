package ru.calculator;

/**
 * Calculator.
 *
 * @author Andrey Kovalko (kovalkoandrey1983@mail.ru)
 * @version $Id$
 * @since 0.1
 */
public class Calculator {

    /**
     * Method add.
     * @param first, second.
     * @return first + second.
     */
    public double add(double first, double second) {
        return first + second;
    }

    /**
     * Method subtract.
     * @param first, second.
     * @return first - second.
     */
    public double subtract(double first, double second) {
        return first - second;
    }

    /**
     * Method div.
     * @param first, second.
     * @return first / second.
     */
    public double div(double first, double second) {
        return first / second;
    }

    /**
     * Method multiple.
     * @param first, second.
     * @return first * second.
     */
    public double multiple(double first, double second) {
        return first * second;
    }
}